package com.javarush.task.task03.task0314;

/* 
Таблица умножения
*/

public class Solution {
    public static void main(String[] args) {
        int a = 1;
        while (a <= 10) {
            for (int i = 1; i <= 10; i++) {
                System.out.print(a * i + " ");
            }
            System.out.println();
            a++;
        }

        //напишите тут ваш код
    }
}
